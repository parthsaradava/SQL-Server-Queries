--I. DISPLAY THE TOTAL FINE AMOUNT COLLECTED FOR EACH BOOK GENRE. 

SELECT BOOKGENRE,SUM(FINEAMOUNT) 
FROM LIBRARYBORROWING
GROUP BY BOOKGENRE

--II. FIND THE AVERAGE NUMBER OF DAYS BORROWED FOR EACH BOOK 
--GENRE. 

SELECT BOOKGENRE,AVG(DAYSBORROWED) AS  DAYBORROWED_AVG
FROM LIBRARYBORROWING
GROUP BY BOOKGENRE
--III. DISPLAY THE NUMBER OF BORROWING RECORDS FOR EACH MEMBER.

SELECT MEMBERNAME , COUNT(BORROWID) AS BORROW_ID
FROM LIBRARYBORROWING
GROUP BY MEMBERNAME
--IV. FIND THE MAXIMUM FINE AMOUNT PAID IN EACH BOOK GENRE.

SELECT BOOKGENRE , MAX(FINEAMOUNT) AS BOOKGENRE
FROM LIBRARYBORROWING
GROUP BY BOOKGENRE
--V. DISPLAY THE MINIMUM DAYS BORROWED FOR EACH BOOK GENRE. 

SELECT BOOKGENRE , MIN(DAYSBORROWED) AS BOOKGENRE
FROM LIBRARYBORROWING
GROUP BY BOOKGENRE


--VI. 
--FIND THE TOTAL NUMBER OF DAYS BOOKS WERE BORROWED BY EACH 
--MEMBER. 

SELECT MEMBERNAME , SUM(DAYSBORROWED) AS MEMBERNAME
FROM LIBRARYBORROWING
GROUP BY MEMBERNAME
--VII. DISPLAY THE AVERAGE FINE AMOUNT PAID BY EACH MEMBER. 
 
 SELECT MEMBERNAME, AVG(FINEAMOUNT) AS MEMBERNAME
FROM LIBRARYBORROWING
