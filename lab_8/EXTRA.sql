--VIII. FIND THE HIGHEST NUMBER OF DAYS BORROWED BY EACH MEMBER.

SELECT MEMBERNAME , MAX(DAYSBORROWED) AS MEMBERNAME
FROM LIBRARYBORROWING
GROUP BY MEMBERNAME
